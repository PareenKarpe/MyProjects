    private void requestTestJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestTestJButtonActionPerformed

        String message = messageTextArea.getText();
        JOptionPane.showMessageDialog(null, "Request is forwarded", "Success", JOptionPane.INFORMATION_MESSAGE);

        SoilTestWorkRequest request = new SoilTestWorkRequest();
        
        request.setMessage(message);
   //     request.setSender(userAccount);
        
        //To change the status of request
        if(enterprise.getEnterpriseType().getValue().toString().equalsIgnoreCase("Municipal"))
        {
            request.setStatus("Sent by Municipal");

        }
        else
        {
            request.setStatus("Sent");
        }
        
        request.setLocation(locTextField.getText());
        //   System.out.print(locationjCombobox.getSelectedItem().toString());
        
        request.setArea(areaTextField.getText());

        Organization org = null;
        
        Enterprise   e = enterprise;
        //The request in the workqueue gets updated for the instance of staff
        for (Organization organization : e.getOrganizationDirectory().getOrganizationList())
        {
            if (organization instanceof staffOrganisation){
                    org = organization;
                    break;
                }
        }
        if (org!=null)
        {
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);

        }
    }//GEN-LAST:event_requestTestJButtonActionPerformed


